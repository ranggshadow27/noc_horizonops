    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                Section::make('')
                    ->heading('')
                    ->schema([
                        Select::make('site_id')
                            ->label('Site ID')
                            ->options(function () {
                                return SiteDetail::pluck('site_name', 'site_id')->mapWithKeys(function ($siteName, $site_id) {
                                    return [$site_id => "$site_id - $siteName"];
                                })->toArray();
                            })
                            ->getSearchResultsUsing(function (string $search): array {
                                $sites = SiteDetail::where('site_id', 'like', "%{$search}%")
                                    ->orWhere('site_name', 'like', "%{$search}%")
                                    ->limit(10)
                                    ->get();
                                return $sites->mapWithKeys(function ($site) {
                                    return [$site->site_id => "{$site->site_id} - {$site->site_name}"];
                                })->toArray();
                            })
                            ->getOptionLabelFromRecordUsing(function (SiteDetail $record): string {
                                return "{$record->site_id} - {$record->site_name}";
                            })
                            ->preload()
                            ->searchable()
                            ->native(false)
                            ->required()
                            ->reactive()
                            ->columnSpanFull()
                            ->placeholder("Select a site ID or site name"),

                        Textarea::make('description')
                            ->required()
                            ->live(onBlur: true)
                            ->columnSpanFull()
                            ->autosize()
                            ->extraAlpineAttributes(['x-model' => 'description', 'x-on:input' => 'updateFields()'])
                            ->afterStateUpdated(function ($state, callable $set) {
                                preg_match('/Nama\s*:\s*([^\n]+)/i', $state, $nameMatch);
                                preg_match('/Telepon\s*:\s*(\d+)/i', $state, $numberMatch);
                                preg_match('/Ticket\s*:\s*([^\n]+)/i', $state, $ttMatch);
                                $set('pic_name', $nameMatch[1] ?? null);
                                $set('pic_number', $numberMatch[1] ?? null);
                                $set('hb_tt_number', $ttMatch[1] ?? null);
                            }),
                        TextInput::make('pic_name')
                            ->label("PIC Name")
                            ->placeholder("This form is autogenerated")
                            // ->readOnly()
                            ->reactive(),
                        TextInput::make('pic_number')
                            ->label("PIC Number")
                            ->placeholder("Please fill description form")
                            // ->readOnly()
                            ->reactive(),
                        TextInput::make('hb_tt_number')
                            ->label("HaloBakti TT Number")
                            ->placeholder("This form is autogenerated")
                            // ->readOnly()
                            ->reactive(),
                    ]),

                Section::make('')
                    ->heading(fn($operation) => $operation === 'edit' ? '' : 'Ticket Progress')
                    ->visible(fn($operation) => $operation === 'edit' || 'view')
                    ->schema([
                        RichEditor::make('new_comment')
                            ->label('Ticket Progress')
                            ->placeholder('Update ticket progress here..')
                            ->toolbarButtons([
                                'bold',
                                'italic',
                                'redo',
                                'strike',
                                'underline',
                                'undo',
                            ])
                            ->reactive()
                            ->visible(fn($operation) => $operation === 'edit'),
                        // FileUpload::make('new_images')
                        //     ->label('Attached Images')
                        //     ->multiple()
                        //     ->disabled() // Disable direct upload, hanya display
                        //     ->openable()
                        //     ->downloadable(),
                        Actions::make([
                            Action::make('save_comment')
                                // ->visible(fn($get, $operation) => $get('new_comment'))
                                ->label('Update Progress')
                                ->action(function ($state, $record, callable $set, $operation, $livewire, $form) {
                                    if ($state['new_comment']) {
                                        $images = $state['new_images'] ?? [];
                                        if ($operation === 'create') {
                                            $form->getLivewire()->data['comments'] = array_merge(
                                                $form->getLivewire()->data['comments'] ?? [],
                                                [[
                                                    'comment' => $state['new_comment'],
                                                    'time' => now()->toDateTimeString(),
                                                    'user_id' => Auth::id() ?? 'System',
                                                    'images' => $images,
                                                ]]
                                            );
                                        } else {
                                            $record->addComment($state['new_comment'], $images);
                                        }
                                        $set('new_comment', '');
                                        $set('new_images', []);
                                        $livewire->uploadedImageCount = 0; // Reset counter
                                    }
                                }),
                            Action::make('upload_image')
                                ->icon('phosphor-upload-simple-duotone')
                                ->color('gray')
                                ->label(fn($get) => !empty($get('new_images')) ? 'Uploaded ' . count($get('new_images')) . ' image(s) ✅' : 'Upload Image')
                                ->disabled(fn($get) => !empty($get('new_images')))
                                ->form([
                                    FileUpload::make('images')
                                        ->label('Images')
                                        ->multiple()
                                        ->panelLayout('grid')
                                        ->disk('public')
                                        ->directory(fn($record) => $record ? "tickets/{$record->ticket_id}/images" : "tickets/MHG-HB-" . now()->format('Ymd') . '-' . str_pad(HaloBaktiTicket::whereDate('created_at', now()->toDateString())->count() + 1, 3, '0', STR_PAD_LEFT) . '/images')
                                        ->preserveFilenames()
                                        ->acceptedFileTypes(['image/jpeg', 'image/png', 'image/gif'])
                                        ->maxFiles(5)
                                        ->maxSize(5120)
                                        // ->getUploadedFileNameForStorageUsing(function (TemporaryUploadedFile $file): string {
                                        //     return (string) str("_comment.{$file->extension()}")->prepend(now()->timestamp);
                                        // })
                                        ->openable()
                                        ->downloadable(),
                                ])
                                ->action(function (array $data, $record, callable $set, callable $get, $form) {
                                    $images = [];
                                    if ($data['images'] ?? []) {
                                        foreach ($data['images'] as $file) {
                                            $images[] = Storage::url($file); // URL final
                                        }
                                        $currentImages = $get('new_images') ?? [];
                                        $set('new_images', array_merge($currentImages, $images));
                                        $form->getLivewire()->uploadedImageCount = count($images);
                                        Log::info('Uploaded files: ' . json_encode($data['images']));
                                        Log::info('New images state: ' . json_encode($get('new_images')));
                                    }
                                })
                                ->modalHeading('Upload Images')
                                ->modalSubmitActionLabel('Save')
                                ->modalWidth('lg'),
                        ])
                            ->visible(fn($operation) => $operation === 'edit'),
                        View::make('filament.pages.ticket-comments')
                            ->viewData(['record' => $form->getRecord()])
                            ->visible(fn($get) => $form->getRecord() !== null),
                    ]),
                Section::make('')
                    ->schema([
                        Select::make('status')
                            ->native(false)
                            ->default('On Progress')
                            ->columnSpanFull()
                            ->options([
                                'On Progress' => 'On Progress',
                                'Closed' => 'Closed',
                                'Unresolved' => 'Unresolved',
                            ]),
                    ])
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->defaultSort('created_at', 'desc')
            // ->recordTitleAttribute('ticket_id')
            ->columns([
                TextColumn::make('hb_tt_number')
                    ->label("Ticket ID/Number")
                    ->copyable()
                    ->description(fn($record): string => $record->ticket_id, 'above')
                    ->searchable(),

                TextColumn::make('site_id')
                    ->hidden()
                    ->searchable(),

                TextColumn::make('site.site_name')
                    ->label('Site Name')
                    ->copyable()
                    ->description(fn($record): string => $record->site_id, 'above')
                    ->limit(20)
                    ->tooltip(function (TextColumn $column): ?string {
                        $state = $column->getState();

                        if (strlen($state) <= $column->getCharacterLimit()) {
                            return null;
                        }

                        return $state;
                    })
                    ->searchable(),

                TextColumn::make('site.province')
                    ->label("Province")
                    ->tooltip(function (TextColumn $column): ?string {
                        $state = $column->getState();

                        if (strlen($state) <= $column->getCharacterLimit()) {
                            return null;
                        }

                        return $state;
                    })
                    ->description(fn($record): string => $record->site->area->area)
                    ->searchable(),

                TextColumn::make('description')
                    ->limit(40),

                TextColumn::make('pic_name')
                    ->hidden()
                    ->searchable(),

                TextColumn::make('pic_number')
                    ->label("PIC Data")
                    ->copyable()
                    ->description(fn($record): string => $record->pic_name ?? "Unknown", 'above')
                    ->copyable(),

                TextColumn::make('status')
                    ->badge()
                    ->icon(function ($state) {
                        if (str_contains($state, "On Progress")) {
                            return "phosphor-spinner-gap-duotone";
                        } elseif (str_contains($state, "Closed")) {
                            return 'phosphor-check-circle-duotone';
                        } elseif (str_contains($state, "Unresolved")) {
                            return 'phosphor-exclamation-mark-duotone';
                        } else {
                            return 'gray';
                        }
                    })
                    ->color(function ($state) {
                        if (str_contains($state, "On Progress")) {
                            return "warning";
                        } elseif (str_contains($state, "Closed")) {
                            return 'success';
                        } elseif (str_contains($state, "Unresolved")) {
                            return 'danger';
                        } else {
                            return 'gray';
                        }
                    }),

                TextColumn::make('created_at')
                    ->dateTime(),
            ])
            ->filters([
                SelectFilter::make('area')
                    ->label("Area")
                    ->options(fn() => AreaList::all()->pluck('area', 'area'))
                    ->native(false)
                    ->modifyQueryUsing(function (Builder $query, $state) {
                        if (! $state['value']) {
                            return $query;
                        }
                        return $query->whereHas('site', fn($query) => $query->whereHas('area', fn($query) => $query->where('area', $state['value'])));
                    }),

                SelectFilter::make('status')
                    ->label("Status")
                    ->options([
                        'pending' => 'Pending',
                        'on progress' => 'On Progress',
                        'unresolved' => 'Unresolved',
                        'closed' => 'Closed',
                    ])
                    ->native(false),

                DateRangeFilter::make('created_at')
                    ->label('Created Date'),
            ])
            ->actions([
                Tables\Actions\EditAction::make()
                    ->label("Update")
                    ->icon("phosphor-pencil-simple-line-duotone")
                    ->modal()
                    ->visible(
                        fn($record) =>
                        $record->status !== 'Closed' && auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4)
                    ),

                Tables\Actions\ViewAction::make()
                    ->label("View Ticket")
                    ->icon("phosphor-eye-duotone")
                    ->modal()
                    ->visible(
                        fn($record) =>
                        $record->status === 'Closed' && auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4)
                    ),

                Tables\Actions\ActionGroup::make([
                    Tables\Actions\Action::make('Zabbix')
                        ->icon('phosphor-monitor-duotone')
                        ->url(fn(HaloBaktiTicket $record): string => "https://manager.zabbix-bakti.io/host/site-tree?site_uniq_id={$record->site_id}&site_type=LAYANAN")
                        ->openUrlInNewTab()
                        ->visible(
                            fn($record) =>
                            $record->status !== 'Closed' && auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4)
                        ),

                    Tables\Actions\Action::make('Grafana')
                        ->icon('phosphor-chart-line-duotone')
                        ->url(function (HaloBaktiTicket $record) {
                            $iphub = $record->site->ip_hub;

                            if ($iphub == 'H58') {
                                return "https://mon-rtgs.mahaga-pratama.co.id/d/-wTRuDqSz/rtgs-h58-remote-terminal-monitoring?orgId=1&refresh=30s&var-Terminal_id={$record->site_id}";
                            };

                            if ($iphub == 'H10') {
                                return "https://mon-rtgs.mahaga-pratama.co.id/d/dGmoBucIk/rtgs-h10-remote-terminal-monitoring?orgId=1&var-Terminal_id={$record->site_id}";
                            };

                            if ($iphub == 'H47') {
                                return "https://mon-rtgs.mahaga-pratama.co.id/d/ffpVJlbHk/rtgs-h47-remote-terminal-monitoring?orgId=1&var-Terminal_id={$record->site_id}";
                            };
                        })
                        ->openUrlInNewTab()
                        ->visible(
                            fn($record) =>
                            $record->status !== 'Closed' && auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4)
                        ),

                    Tables\Actions\Action::make('Details')
                        ->icon('heroicon-c-arrow-up-left')
                        ->label("Sensor Details")
                        ->infolist([
                            Infolists\Components\Section::make('Site Information')
                                ->schema([
                                    TextEntry::make('site_id')
                                        ->label("Site ID"),

                                    TextEntry::make('site.site_name')
                                        ->label("Site Name"),

                                    TextEntry::make('site.gateway')
                                        ->label("Gateway"),

                                    TextEntry::make('site.province')
                                        ->label("Province"),
                                ])
                                ->columns(),

                            Infolists\Components\Section::make('Device Status')
                                ->schema([
                                    TextEntry::make('site.siteMonitor.modem')->badge()->label("Modem")
                                        ->color(fn(string $state): string => match ($state) {
                                            'Down' => 'danger',
                                            'Failed' => 'gray',
                                            'Up' => 'success',
                                        }),
                                    TextEntry::make('site.siteMonitor.mikrotik')->badge()->label("Router")
                                        ->color(fn(string $state): string => match ($state) {
                                            'Down' => 'danger',
                                            'Failed' => 'gray',
                                            'Up' => 'success',
                                        }),
                                    TextEntry::make('site.siteMonitor.ap1')->badge()->label("Access Point 1")
                                        ->color(fn(string $state): string => match ($state) {
                                            'Down' => 'danger',
                                            'Failed' => 'gray',
                                            'Up' => 'success',
                                        }),
                                    TextEntry::make('site.siteMonitor.ap2')->badge()->label("Access Point 2")
                                        ->color(fn(string $state): string => match ($state) {
                                            'Down' => 'danger',
                                            'Failed' => 'gray',
                                            'Up' => 'success',
                                        }),
                                    TextEntry::make('site.siteMonitor.modem_last_up')->badge()->label("Modem Last Up")
                                        ->dateTimeTooltip()->since()->default(Carbon::now()),
                                    TextEntry::make('site.siteMonitor.mikrotik_last_up')->badge()->label("Router Last Up")
                                        ->dateTimeTooltip()->since()->default(Carbon::now()),
                                    TextEntry::make('site.siteMonitor.ap1_last_up')->badge()->label("Access Point 1 Last Up")
                                        ->dateTimeTooltip()->since()->default(Carbon::now()),
                                    TextEntry::make('site.siteMonitor.ap2_last_up')->badge()->label("Access Point 2 Last Up")
                                        ->dateTimeTooltip()->since()->default(Carbon::now()),
                                ])
                                ->columns(4),
                        ])
                        ->modalSubmitAction(false)
                        ->modalHeading('Site Details')
                        ->visible(
                            fn($record) =>
                            $record->status !== 'Closed' && auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4)
                        ),

                    Tables\Actions\Action::make('Generate Autochat')
                        ->label("Generate Autochat")
                        ->icon('phosphor-copy-duotone')
                        ->action(function (HaloBaktiTicket $record, $livewire) {
                            $timeOfDay = now()->hour < 5 ? 'Malam' : (now()->hour < 11 ? 'Pagi' : (now()->hour < 15 ? 'Siang' : (now()->hour < 18 ? 'Sore' : 'Malam')));

                            $report = "Selamat {$timeOfDay} Bapak/Ibu \n\n" .
                                "Kami dari PT Mahaga Penyedia Wifi Bakti dilokasi *{$record->site?->site_name} - {$record->site?->province}* - Maluku Utara, Saat ini kami mendapat informasi kalau akses internet Wifi Bakti dilokasi sedang terkendala, benar Bapak/Ibu?";

                            $livewire->js("navigator.clipboard.writeText(" . json_encode($report) . ");");

                            Notification::make()
                                ->title('Generate Autochat')
                                ->body('Autochat Copied to Clipboard Successfully')
                                ->success()
                                ->send();
                        })
                        ->visible(
                            fn($record) =>
                            $record->status !== 'Closed' && auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4)
                        ),

                    Tables\Actions\DeleteAction::make()
                        ->label("Delete")
                        ->icon("phosphor-trash-duotone")
                        ->visible(
                            fn($record) =>
                            $record->status !== 'Closed' && auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4)
                        ),
                ])
            ])
            ->headerActions([
                CreateAction::make()
                    ->label("Add New Ticket")
                    ->icon('phosphor-plus-circle-duotone')
                    ->visible(fn() => auth()->user()->roles->pluck('id')->some(fn($id) => $id < 4) ? true : false),
            ])
            ->bulkActions([
                BulkActionGroup::make([
                    DeleteBulkAction::make(),
                ]),
            ])
            ->heading('Halo Bakti RTGS BAKTI Ticket')
            ->description('Mahaga Halo Bakti Trouble Ticket - Network Operation Center.')
            ->defaultPaginationPageOption(10)
            ->paginationPageOptions([5, 10, 20, 100])
            ->emptyStateHeading('No Assigned Ticket yet')
            ->emptyStateDescription('Once you have been assign your first Ticket, it will appear here.')
            ->emptyStateIcon('phosphor-hand-withdraw-duotone');
    }

